create or replace function public.apply_project_invoice_status_rules()
returns trigger
language plpgsql
as $$
begin
  if new.status = 'draft' and new.sent_at is not null then
    new.status = 'issued';
  end if;

  if new.status <> 'paid' and new.due_date is not null and new.due_date < current_date then
    new.status = 'overdue';
  end if;

  if new.status in ('issued', 'paid') and new.sent_at is null then
    new.sent_at = now();
  end if;

  if new.status = 'draft' then
    new.sent_at = null;
  end if;

  return new;
end;
$$;

drop trigger if exists project_invoices_apply_status_rules on public.project_invoices;

create trigger project_invoices_apply_status_rules
before insert or update on public.project_invoices
for each row
execute function public.apply_project_invoice_status_rules();

update public.project_invoices
