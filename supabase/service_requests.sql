-- Clients can read their own service requests (matched by email)
drop policy if exists "service_requests_client_select" on public.service_requests;
create policy "service_requests_client_select"
on public.service_requests
for select
to authenticated
using (
  email = (auth.jwt() ->> 'email')
  and (auth.jwt() -> 'app_metadata' ->> 'role') is distinct from 'admin'
);

-- Clients can read their own projects (matched by customer_email)
drop policy if exists "customer_projects_client_select" on public.customer_projects;
create policy "customer_projects_client_select"
on public.customer_projects
for select
to authenticated
using (
  customer_email = (auth.jwt() ->> 'email')
  and (auth.jwt() -> 'app_metadata' ->> 'role') is distinct from 'admin'
);

-- Clients can read deliverables for their own projects
drop policy if exists "project_deliverables_client_select" on public.project_deliverables;
create policy "project_deliverables_client_select"
on public.project_deliverables
for select
to authenticated
using (
  (auth.jwt() -> 'app_metadata' ->> 'role') is distinct from 'admin'
  and project_id in (
    select id from public.customer_projects
    where customer_email = (auth.jwt() ->> 'email')
  )
);

-- Clients can read customer-visible timeline events for their own projects
drop policy if exists "project_timeline_client_select" on public.project_timeline_events;
create policy "project_timeline_client_select"
on public.project_timeline_events
for select
to authenticated
using (
  is_customer_visible = true
  and (auth.jwt() -> 'app_metadata' ->> 'role') is distinct from 'admin'
  and project_id in (
    select id from public.customer_projects
    where customer_email = (auth.jwt() ->> 'email')
  )
);
